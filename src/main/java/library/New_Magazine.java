    private void bt_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_addActionPerformed
        String aux = tf_stock.getText();
        boolean numStok = true;
        
        if (tf_numMagazine.getText().trim().equals("") ||
            tf_date.getText().trim().equals("") ||
            tf_volume.getText().trim().equals("") ||
            tf_stock.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "!!Complete the data¡¡");
        }else{
            for (int i = 0; i < aux.length(); i++) {
                char ch = aux.charAt(i);
                if ( !Character.isDigit(ch) ) numStok = false;
            }
            
            if ( numStok ){
                data.addMagazine(tf_numMagazine.getText(), tf_volume.getText(), 
                                 tf_date.getText(), tf_stock.getText());
                JOptionPane.showMessageDialog(null, "Registered magazine");
                dispose();
            }else
               JOptionPane.showMessageDialog(null, "!!Invalid Stock¡¡"); 
        }
    }//GEN-LAST:event_bt_addActionPerformed


